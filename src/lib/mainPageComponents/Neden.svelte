  <h3>Seanslar ne kadar sürer ve ne sıklıkla yapılır?</h3>
  <p>
    Bireysel seanslar genellikle 50 dakika sürer. Seans sıklığı, ihtiyaçlarınıza
    göre haftalık veya iki haftada bir olarak planlanır.
  </p>
  <h3>Terapi ne kadar sürede etkili olur?</h3>
  <p>
    Terapinin süresi, kişinin hedeflerine ve ihtiyaçlarına göre değişir. Benim
    yaklaşımım genellikle uzun soluklu bir süreçtir, çünkü yalnızca semptomları
    hafifletmek yerine, kök nedenlere inerek kalıcı bir değişim hedeflerim. Bazı
    danışanlar semptomlarında hızlı bir iyileşme görebilirken, daha derin
    çalışmalarda sabırlı olmak ve zamana yaymak gerekebilir. Bu süreçte,
    danışanın işbirliği ve kararlılığı önemli bir rol oynar.
  </p>
  <h3>Hangi konularda destek alabilirim?</h3>
  <p>
    Kaygı ve stres yönetimi, depresyon, yas süreci, ilişkilerde yaşanan
    zorluklar, duygusal dayanıklılık geliştirme ve otoimmün hastalıklarla başa
    çıkma konularında destek alabilirsiniz.
  </p>

  <h3>Online seanslar yüz yüze seanslar kadar etkili mi?</h3>

  <p>
    Evet, yapılan araştırmalar online terapi seanslarının yüz yüze terapi kadar
    etkili olabileceğini göstermektedir. Önemli olan, terapinin nerede
    yapıldığından çok, danışanın bu sürece açık olması ve güvenli bir ortam
    yaratılmasıdır. Online seanslar sırasında da aynı özen ve gizlilik
    prensipleriyle çalışıyorum.
  </p>

  <h3>İlk seansa nasıl hazırlanmalıyım?</h3>

  <p>
    İlk seansta, geçmiş deneyimleriniz, mevcut zorluklarınız ve hedefleriniz
    hakkında konuşacağız. Özel bir hazırlık yapmanıza gerek yoktur; kendinizi
    rahat hissedeceğiniz şekilde gelmeniz yeterlidir.
  </p>
